package net.lyx.dbframework.testengine.dao.entity;

import lombok.Builder;
import lombok.EqualsAndHashCode;
import lombok.ToString;
import net.lyx.dbframework.dao.entity.EntityAccessible;
import net.lyx.dbframework.dao.entity.EntityAutoGenerated;
import net.lyx.dbframework.dao.entity.EntityElement;

@Builder
@ToString
@EqualsAndHashCode
@EntityAccessible(name = "Users")
public class User {

    @EntityAutoGenerated
    private long id;

    @EntityElement(order = 2)
    private String lastName;

    @EntityElement(order = 1)
    private String firstName;

    @EntityElement
    private Status status;
}
